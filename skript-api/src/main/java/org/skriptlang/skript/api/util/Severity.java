package org.skriptlang.skript.api.util;

/**
 * Represents the severity of a diagnostic message.
 */
public enum Severity {
	/**
	 * A message that generally provides information such as platform and environment.
	 */
	INFO,

	/**
	 * A message that may indicate a potential issue.
	 */
	WARNING,

	/**
	 * A message that indicates an error.
	 */
	ERROR
}
